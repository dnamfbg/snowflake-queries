            THEN 1 - (MAX(EXPECTED_TRADING_WIN_BY_WAGER) OVER (PARTITION BY WAGER_ID) /
                      MAX(TOTAL_STAKE_BY_WAGER)           OVER (PARTITION BY WAGER_ID))
