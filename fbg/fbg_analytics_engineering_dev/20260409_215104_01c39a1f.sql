        FIRST_VALUE(EVENT_ID)               OVER (PARTITION BY WAGER_ID ORDER BY LEG_ID) AS first_event_id,
        FIRST_VALUE(EVENT_NAME)             OVER (PARTITION BY WAGER_ID ORDER BY LEG_ID) AS first_event_name,
        FIRST_VALUE(EVENT_SPORT_NAME)       OVER (PARTITION BY WAGER_ID ORDER BY LEG_ID) AS first_event_sport,
        FIRST_VALUE(MARKET_WINNER_CATEGORY) OVER (PARTITION BY WAGER_ID ORDER BY LEG_ID) AS first_market_winner_category,
        CASE
            WHEN MAX(TOTAL_STAKE_BY_WAGER) OVER (PARTITION BY WAGER_ID) IS NOT NULL
