-- Step 1: Base merged table
base AS (
    SELECT
        t.ACCOUNT_ID, t.WAGER_ID, t.LEG_ID,
        t.WAGER_PLACED_TIME_EST,
        t.EXPECTED_HOLD_PCT_BY_WAGER, t.TOTAL_EXPECTED_HOLD_PCT_BY_WAGER,
        t.EXPECTED_NGR_BY_WAGER, t.EXPECTED_GGR_BY_WAGER,
        t.TOTAL_STAKE_BY_WAGER, t.TOTAL_CASH_STAKE_BY_WAGER,
        t.TOTAL_PRICE_BY_WAGER, t.TOTAL_WINNINGS_BY_WAGER,
        t.PROMOTION_BOOST_PERCENTAGE, t.NON_BOOSTED_BET_PRICE,
        t.LEG_CLOSING_LINE_VALUE, t.LEG_ODDS,
        t.NUMBER_OF_LINES_BY_WAGER, t.LEG_PROBABILITY,
        t.WAGER_BET_TYPE, t.EVENT_NAME, t.EVENT_ID,
        t.EVENT_SPORT_NAME, t.WAGER_PUNTER_COLOR, t.WAGER_STATE,
        t.PROMOTION_SETTLEMENT_TYPE, t.MARKET_WINNER_CATEGORY,
        t.ACCOUNT_VALUE_BAND_AS_OF_PLACEMENT,
        t.EXPECTED_TRADING_WIN_BY_WAGER,
        s.BETTYPE,
        s.SUBBETS_SAMEGAMEPARLAY_PLACEMENTPROBABILITY,
        s.SUBBETS_SAMEGAMEPARLAY_POTENTIALPAYOUTPROBABILITY,
        s.SUBBETS_SINGLELEG_PROBABILITY,
        s.SUBBETS_SAMEGAMEPARLAY_LEGS_EVENTID,
        s.SUBBETS_SAMEGAMEPARLAY_LEGS_ID,
    FROM FBG_ANALYTICS_ENGINEERING.TRADING.TRADING_SPORTSBOOK_MART AS t
    LEFT JOIN (
    SELECT
        s.*,
        CASE
            WHEN s.BETTYPE IN ('BET_TYPE_SAME_GAME_PARLAY', 'BET_TYPE_SAME_GAME_PARLAY_STACK')
                THEN s.SUBBETS_SAMEGAMEPARLAY_LEGS_ID
