        NUMBER_OF_LINES_BY_WAGER, TOTAL_PRICE_BY_WAGER, TOTAL_STAKE_BY_WAGER,
        TOTAL_CASH_STAKE_BY_WAGER, TOTAL_WINNINGS_BY_WAGER,
        CASE WHEN TOTAL_WINNINGS_BY_WAGER > 0 THEN 1 ELSE 0 END AS WAGER_WIN,
        CASE
            WHEN TOTAL_CASH_STAKE_BY_WAGER IS NULL OR TOTAL_CASH_STAKE_BY_WAGER = 0 THEN NULL
            ELSE TOTAL_WINNINGS_BY_WAGER / TOTAL_CASH_STAKE_BY_WAGER
        END AS PAYOUT_PER_DOLLAR,
        EXPECTED_HOLD_PCT_BY_WAGER, TOTAL_EXPECTED_HOLD_PCT_BY_WAGER,
        EXPECTED_NGR_BY_WAGER, EXPECTED_GGR_BY_WAGER,
        PROMOTION_BOOST_PERCENTAGE, NON_BOOSTED_BET_PRICE,
        PROMOTION_SETTLEMENT_TYPE, WAGER_PUNTER_COLOR,
        ACCOUNT_VALUE_BAND_AS_OF_PLACEMENT,
        SHARP,
        FIRST_VALUE(EV) OVER (PARTITION BY WAGER_ID ORDER BY EV IS NOT NULL DESC, LEG_ID)                              AS EV,
        FIRST_VALUE(EV_SINGLE_BET) OVER (PARTITION BY WAGER_ID ORDER BY EV_SINGLE_BET IS NOT NULL DESC, LEG_ID)        AS EV_SINGLE_BET,
