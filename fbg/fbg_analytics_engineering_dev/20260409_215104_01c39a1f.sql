    LEFT JOIN ev_potential_outcome AS po  ON b.ACCOUNT_ID = po.ACCOUNT_ID  AND b.WAGER_ID = po.WAGER_ID
    LEFT JOIN FBG_ANALYTICS.TRADING.SHARP_MODEL_FEATURES AS sh ON b.ACCOUNT_ID = sh.ACCO_ID
),
-- Step 3: Collapse to wager level
wager_level AS (
