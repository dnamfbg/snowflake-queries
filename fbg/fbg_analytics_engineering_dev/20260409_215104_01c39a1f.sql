),
-- Step 2a: EV straight parlay
straight_parlay_ev AS (
    SELECT ACCOUNT_ID, WAGER_ID,
        CASE
            WHEN COUNT_IF(LEG_ODDS IS NULL OR LEG_ODDS <= 0) > 0 THEN NULL
            WHEN COUNT_IF(LEG_PROBABILITY IS NULL OR LEG_PROBABILITY < 0 OR LEG_PROBABILITY > 1) > 0 THEN NULL
            ELSE REDUCE(ARRAY_AGG(LEG_ODDS), 1, (acc, x) -> acc * x) *
                 REDUCE(ARRAY_AGG(LEG_PROBABILITY), 1, (acc, x) -> acc * x)
        END AS EV_STRAIGHT_PARLAY
    FROM base WHERE WAGER_BET_TYPE = 'MULTIPLE'
    GROUP BY ACCOUNT_ID, WAGER_ID
),
event_probs AS (
    SELECT ACCOUNT_ID, WAGER_ID, EVENT_ID,
        COUNT(*) AS legs_per_event,
        CASE WHEN COUNT(*) > 1 THEN MAX(SUBBETS_SAMEGAMEPARLAY_PLACEMENTPROBABILITY)
             ELSE MAX(LEG_PROBABILITY) END AS EVENT_PROBABILITY
    FROM base WHERE WAGER_BET_TYPE = 'SGP Stack' AND EVENT_ID IS NOT NULL
    GROUP BY ACCOUNT_ID, WAGER_ID, EVENT_ID
),
sgp_stack_ev AS (
    SELECT ACCOUNT_ID, WAGER_ID,
        CASE WHEN COUNT_IF(EVENT_PROBABILITY IS NULL OR EVENT_PROBABILITY < 0 OR EVENT_PROBABILITY > 1) > 0 THEN NULL
             ELSE REDUCE(ARRAY_AGG(EVENT_PROBABILITY), 1, (acc, x) -> acc * x)
        END AS PROBABILITY_PRODUCT
    FROM event_probs GROUP BY ACCOUNT_ID, WAGER_ID
),
ev_potential_outcome AS (
    SELECT ACCOUNT_ID, WAGER_ID,
        SUM(LEG_ODDS * LEG_PROBABILITY) / COUNT(*) AS EV_POTENTIAL_OUTCOME
    FROM base WHERE LEG_ODDS IS NOT NULL AND LEG_PROBABILITY IS NOT NULL
    GROUP BY ACCOUNT_ID, WAGER_ID
),
with_ev AS (
    SELECT b.*,
        CASE
            WHEN b.WAGER_PLACED_TIME_EST < '2025-06-10' THEN NULL
            WHEN b.WAGER_BET_TYPE = 'SINGLE'     THEN b.LEG_ODDS * b.LEG_PROBABILITY
            WHEN b.WAGER_BET_TYPE = 'MULTIPLE'   THEN sp.EV_STRAIGHT_PARLAY
            WHEN b.WAGER_BET_TYPE = 'BETBUILDER' THEN b.TOTAL_PRICE_BY_WAGER * b.SUBBETS_SAMEGAMEPARLAY_POTENTIALPAYOUTPROBABILITY
            WHEN b.WAGER_BET_TYPE = 'SGP Stack'  THEN b.TOTAL_PRICE_BY_WAGER * sgp.PROBABILITY_PRODUCT
            ELSE NULL
        END AS EV,
        CASE WHEN b.WAGER_PLACED_TIME_EST < '2025-06-10' THEN NULL
             ELSE b.LEG_ODDS * b.LEG_PROBABILITY END AS EV_SINGLE_BET,
        CASE WHEN b.WAGER_PLACED_TIME_EST < '2025-06-10' THEN NULL
             ELSE po.EV_POTENTIAL_OUTCOME END AS EV_POTENTIAL_OUTCOME,
        sh.SHARP
    FROM base AS b
    LEFT JOIN straight_parlay_ev   AS sp  ON b.ACCOUNT_ID = sp.ACCOUNT_ID  AND b.WAGER_ID = sp.WAGER_ID  AND b.WAGER_BET_TYPE = 'MULTIPLE'
