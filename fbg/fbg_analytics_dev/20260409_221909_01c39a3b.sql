SELECT 
    CASE 
        WHEN DAY(DATE) BETWEEN 1 AND 8 THEN 'Week 1'
        WHEN DAY(DATE) BETWEEN 9 AND 15 THEN 'Week 2'
        WHEN DAY(DATE) BETWEEN 16 AND 22 THEN 'Week 3'
        WHEN DAY(DATE) >= 23 THEN 'Week 4'
    END AS WEEK_LABEL,
    SUM(OSB_MARKET_ACCESS_FEE + OC_MARKET_ACCESS_FEE) / 1000000 AS MA_FEE,
    SUM(OSB_FINANCE_GGR + OC_FINANCE_GGR) / 1000000 AS TOTAL_GGR,
    -- 10% marketing deduction from GGR
    SUM(OSB_FINANCE_GGR + OC_FINANCE_GGR) * 0.10 / 1000000 AS MARKETING_DEDUCTION,
    -- Adjusted GGR after marketing deduction
    SUM(OSB_FINANCE_GGR + OC_FINANCE_GGR) * 0.90 / 1000000 AS ADJUSTED_GGR
FROM FBG_ANALYTICS.PRODUCT_AND_CUSTOMER.CUSTOMER_VARIABLE_PROFIT
WHERE DATE BETWEEN '2025-03-01' AND '2025-03-31'
  AND STATE = 'CT'
GROUP BY WEEK_LABEL
ORDER BY WEEK_LABEL
