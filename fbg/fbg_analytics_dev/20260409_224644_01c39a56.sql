CREATE OR REPLACE TEMPORARY TABLE Cas_Lifetime AS
SELECT c.acco_id, 
SUM(GGR) AS Cas_GGR
FROM FBG_ANALYTICS_ENGINEERING.casino.casino_daily_settled_agg C
INNER JOIN FBG_SOURCE.OSB_SOURCE.Accounts A
ON A.ID = C.Acco_ID
WHERE A.Test = 0
GROUP BY ALL
