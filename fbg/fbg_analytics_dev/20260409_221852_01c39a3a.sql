SELECT 
    STATE,
    SUM(OSB_MARKET_ACCESS_FEE + OC_MARKET_ACCESS_FEE) / 1000000 AS MARKET_ACCESS_FEES,
    SUM(OSB_FINANCE_GGR + OC_FINANCE_GGR) / 1000000 AS TOTAL_GGR,
    SUM(OSB_MARKET_ACCESS_FEE + OC_MARKET_ACCESS_FEE) / NULLIF(SUM(OSB_FINANCE_GGR + OC_FINANCE_GGR), 0) * 100 AS MA_PCT_OF_GGR
FROM FBG_ANALYTICS.PRODUCT_AND_CUSTOMER.CUSTOMER_VARIABLE_PROFIT
WHERE DATE BETWEEN '2025-03-01' AND '2025-03-31'
  AND STATE = 'CT'
GROUP BY STATE
