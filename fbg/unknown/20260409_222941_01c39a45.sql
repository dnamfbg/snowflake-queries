SELECT
    CONTACT_CHANNEL_V2,
    COUNT(*) AS TOTAL_CASES,
    SUM(CASE_TIME_MINUTES) AS TOTAL_ACTIVE_MINUTES,
    AVG(CASE_TIME_MINUTES) AS AVG_ACTIVE_TIME_PER_CASE
FROM FBG_ANALYTICS.OPERATIONS.CS_CASES
WHERE CONTACT_CHANNEL_V2 IN ('Chat', 'Email')
GROUP BY CONTACT_CHANNEL_V2
ORDER BY TOTAL_CASES DESC
