SELECT
    CRYPTO.PROCESS_DATE,
    CRYPTO.SOD_BALANCE, CRYPTO.PAYMENTS, CRYPTO.WITHDRAWALS,
    CRYPTO.TOTAL_OPEN_ORDERS, CRYPTO.TOTAL_CLOSE_ORDERS,
    CRYPTO.SETTLEMENT_FEES, CRYPTO.TRADE_FEES,
    CRYPTO.TOTAL_SETTLEMENT_ORDERS, CRYPTO.ADJUSTMENTS,
    CRYPTO.EOD_BALANCE, CRYPTO.NET_TRANSFER_DUE,
    FMX.SOD_BAL, FMX.PAYMENT, FMX.WITHDRAWALS AS FMX_WD,
    FMX.OPEN_ORDERS, FMX.CLOSED_ORDERS,
    FMX.TRADE_FEE, FMX.SETTLEMENT, FMX.END_BAL
FROM FBG_SOURCE.CRYPTO.TSP_EOD_SUMMARY CRYPTO
JOIN FBG_FINANCE.STAGING.FMX_RECONCILIATION_REPORT_DAILY FMX
  ON CRYPTO.PROCESS_DATE = FMX.TRANS_DATE_EST
WHERE CRYPTO.PROCESS_DATE IN ('2025-12-23', '2025-12-26', '2025-12-29', '2026-01-02', '2026-01-05', '2026-01-09', '2026-01-20', '2026-02-10', '2026-02-25', '2026-02-26', '2026-02-27')
ORDER BY PROCESS_DATE
